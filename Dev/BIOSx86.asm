;; Aqui iremos utilizar os serviços do BIOS para provocar um atraso

executarAtraso:

    mov al, 0
    mov ah, 86h  ;; Função de causar atraso
    mov cx, 1    ;; CX:DX - tempo, em microssegundos

    int 15h      ;; Chamar BIOS

    ret

    